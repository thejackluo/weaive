"""
Database Table Pattern with Soft Delete:

CREATE TABLE goals (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    user_id UUID NOT NULL REFERENCES user_profiles(id),
    title TEXT NOT NULL,
    description TEXT,
    emoji TEXT NOT NULL,
    is_archived BOOLEAN DEFAULT FALSE,
    status TEXT NOT NULL DEFAULT 'active',

    -- Standard timestamps
    created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
    updated_at TIMESTAMPTZ NOT NULL DEFAULT now(),
    deleted_at TIMESTAMPTZ DEFAULT NULL,  -- Soft delete

    CONSTRAINT goals_status_check CHECK (status IN ('active', 'completed', 'abandoned'))
);

-- Index for soft delete filtering
CREATE INDEX idx_goals_deleted_at ON goals(deleted_at) WHERE deleted_at IS NULL;

-- Index for user queries (only active records)
CREATE INDEX idx_goals_user_active ON goals(user_id, deleted_at) WHERE deleted_at IS NULL;

-- Trigger to auto-update updated_at
CREATE OR REPLACE FUNCTION update_updated_at_column()
RETURNS TRIGGER AS $$
BEGIN
    NEW.updated_at = now();
    RETURN NEW;
END;
$$ language 'plpgsql';

CREATE TRIGGER update_goals_updated_at BEFORE UPDATE ON goals
    FOR EACH ROW EXECUTE FUNCTION update_updated_at_column();

-- RLS Policies (Story 0.4)
ALTER TABLE goals ENABLE ROW LEVEL SECURITY;

CREATE POLICY "users_manage_own_goals" ON goals
    FOR ALL
    USING (user_id IN (
        SELECT id FROM user_profiles WHERE auth_user_id = auth.uid()::text
    ));

-- Application Code Pattern:

# Soft delete (set deleted_at timestamp)
supabase.table('goals').update({
    'deleted_at': datetime.utcnow().isoformat()
}).eq('id', goal_id).execute()

# Query active records only (exclude soft deleted)
supabase.table('goals').select('*').is_('deleted_at', 'null').execute()

# Restore soft deleted record
supabase.table('goals').update({
    'deleted_at': None
}).eq('id', goal_id).execute()

# Hard delete (permanent removal - use with caution!)
supabase.table('goals').delete().eq('id', goal_id).execute()
"""


