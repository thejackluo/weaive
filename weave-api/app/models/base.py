"""
Database Naming (PostgreSQL/Supabase):
- Tables: snake_case, plural (e.g., user_profiles, journal_entries)
- Columns: snake_case (e.g., user_id, local_date, scheduled_for_date)
- Foreign keys: {table}_id (e.g., user_id, goal_id)
- Indexes: idx_{table}_{columns} (e.g., idx_completions_user_date)
- Constraints: {table}_{column}_check (e.g., goals_status_check)

Python Models (Pydantic):
- Classes: PascalCase (e.g., GoalCreate, UserProfileResponse)
- Fields: snake_case to match database columns
- Files: snake_case (e.g., goal.py, user_profile.py)

API Endpoints (FastAPI):
- Routes: kebab-case for multi-word resources (e.g., /api/journal-entries)
- Query params: snake_case (e.g., ?user_id=xxx&local_date=2025-12-23)
"""
